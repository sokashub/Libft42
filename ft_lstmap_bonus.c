#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*new_elem;
	t_list	*curr_elem;

	if (!lst || !f)
		return (NULL);
	new_list = 0;
	while (lst)
	{
		new_elem = ft_lstnew(f(lst->content));
		if (!new_elem)
		{
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		if (!new_list)
			new_list = new_elem;
		else
			curr_elem->next = new_elem;
		curr_elem = new_elem;
		lst = lst->next;
	}
	return (new_list);
}
